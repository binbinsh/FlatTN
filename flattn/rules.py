def get_skip_path(chars, w_trie):
    sentence = ''.join(chars)
    result = w_trie.get_lexicon(sentence)
    return result


def get_skip_path_trivial(chars, w_list):
    chars = ''.join(chars)
    w_set = set(w_list)
    result = []
    for i in range(len(chars) - 1):
        for j in range(i+2, len(chars) + 1):
            if chars[i:j] in w_set:
                result.append([i, j-1, chars[i:j]])
    return result


class TrieNode:
    def __init__(self):
        self.children = collections.defaultdict(TrieNode)
        self.is_w = False


# HERE to add the rules lexicon
class Trie:
    def __init__(self):
        self.root = TrieNode()

    def insert(self, w):
        current = self.root
        for c in w:
            current = current.children[c]
        current.is_w = True

    def search(self, w):
        current = self.root
        for c in w:
            current = current.children.get(c)
            if current is None:
                return -1
        if current.is_w:
            return 1
        else:
            return 0

    def get_lexicon(self, sentence):
        result = []
        for i in range(len(sentence)):
            current = self.root
            for j in range(i, len(sentence)):
                current = current.children.get(sentence[j])
                if current is None:
                    break
                if current.is_w:
                    result.append([i, j, sentence[i:j+1]])
        return result


